class Validator:
    """
    Validates the input data from the csvfile.
    """
    def __init__(self, value):
        self.value = value

    def is_email(self):
        """
        validates that input is an email.
        """
        self.value
        return False

    def is_date(self):
        """
        validates that input is a date.
        """
        self.value
        return True

    def is_link(self):
        """
        validates that input is a link.
        """
        self.value
        return True

    def is_text(self):
        """
        validates that input is a text.
        """
        self.value
        return True
